//头部检索 
 function LTrim(s)
{
    for(var i=0;i<s.length;i++)
        if(s.charAt(i)!=' ')
         return s.substring(i,s.length);
     return "";
}
function RTrim(s)
{
    for(var i=s.length-1;i>=0;i--)
        if(s.charAt(i)!=' ')
            return s.substring(0,i+1);
        return "";
}
function Trim(s)
{
    return RTrim(LTrim(s));
}
function submit_search()
{
        var site_form = document.getElementById("allsitesearch");
        var doctitle =  site_form.site_keywords.value;
        var searchword = '';
        doctitle= doctitle.replace(new RegExp('，',"gm"),''); 
        doctitle= doctitle.replace(new RegExp(' ',"gm"),''); 
        if(doctitle.indexOf("+")>=0){
        alert("搜索词中不能含有关键字'+'");
        return false;
        }else if(doctitle.indexOf("*")>=0){
        alert("搜索词中不能含有关键字'*'");
        return false;
        }
        if(doctitle.length ==0 || doctitle=="请输入关键字"){
        alert("请输入关键字！");
        return false;
        }
                
        if(doctitle.length > 0) {
        searchword += doctitle;
        }               
        document.getElementById("site_searchword1").value = searchword;
}